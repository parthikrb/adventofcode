/**
 * A - Rock
 * B - Paper
 * C - Scissors
 * X - Rock
 * Y - Paper
 * Z - Scissors
 */

const shapeScore = {
  X: 1,
  Y: 2,
  Z: 3,
};

const rockPaperScissors = (input) => {
  const splitStrategy = input.split("\n").map((item) => item.split(" ")); // [['A', 'Y'], ['B', 'X'], ['C', 'Z']]
  return splitStrategy.reduce((acc, curr) => {
    const [player1, player2] = curr;
    const score = getScore(player1, player2);
    return acc + score;
  }, 0);
};

const getScore = (player1, player2) => {
  let score = 0;

  // base case when both players choose the same shape
  if (player1 === "A" && player2 === "X") score = 3 + shapeScore[player2];
  if (player1 === "B" && player2 === "Y") score = 3 + shapeScore[player2];
  if (player1 === "C" && player2 === "Z") score = 3 + shapeScore[player2];

  // when player 1 chooses rock and player 2 chooses paper
  if (player1 === "A" && player2 === "Y") score = 6 + shapeScore[player2];
  // when player 1 chooses rock and player 2 chooses scissors
  if (player1 === "A" && player2 === "Z") score = shapeScore[player2];

  // when player 1 chooses paper and player 2 chooses rock
  if (player1 === "B" && player2 === "X") score = shapeScore[player2];
  // when player 1 chooses paper and player 2 chooses scissors
  if (player1 === "B" && player2 === "Z") score = 6 + shapeScore[player2];

  // when player 1 chooses scissors and player 2 chooses rock
  if (player1 === "C" && player2 === "X") score = 6 + shapeScore[player2];
  // when player 1 chooses scissors and player 2 chooses paper
  if (player1 === "C" && player2 === "Y") score = shapeScore[player2];

  return score;
};

