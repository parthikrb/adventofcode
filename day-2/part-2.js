/**
 * A - Rock
 * B - Paper
 * C - Scissors
 * X - Lose
 * Y - Draw
 * Z - Win
 */

const shapeScore = {
  A: 1,
  B: 2,
  C: 3,
};

const rockPaperScissors = (input) => {
  const splitStrategy = input.split("\n").map((item) => item.split(" ")); // [['A', 'Y'], ['B', 'X'], ['C', 'Z']]
  return splitStrategy.reduce((acc, curr) => {
    const [player1, player2] = curr;
    const score = getScore(player1, player2);
    return acc + score;
  }, 0);
};

const getScore = (player1, player2) => {
  let score = 0;
  const winningScore = 6;
  const drawScore = 3;

  // Player 2 loses
  if (player1 === "A" && player2 === "X") score = shapeScore.C; // Scissors
  if (player1 === "B" && player2 === "X") score = shapeScore.A; // Rock
  if (player1 === "C" && player2 === "X") score = shapeScore.B; // Paper

  // Player 2 wins
  if (player1 === "A" && player2 === "Z") score = winningScore + shapeScore.B; // Paper
  if (player1 === "B" && player2 === "Z") score = winningScore + shapeScore.C; // Scissors
  if (player1 === "C" && player2 === "Z") score = winningScore + shapeScore.A; // Rock

  // Player 2 draws
  if (player1 === "A" && player2 === "Y") score = drawScore + shapeScore.A; // Rock
  if (player1 === "B" && player2 === "Y") score = drawScore + shapeScore.B; // Paper
  if (player1 === "C" && player2 === "Y") score = drawScore + shapeScore.C; // Scissors

  return score;
};

